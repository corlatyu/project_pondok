    <h1>PROFIL PONDOK PESANTREN AL AKHYAR</h1>

    <p>Pondok pesantren al akhyar menurut manuskrip catatan yang ditulis oleh KH. Muhammad 
        sholeh (1250-1337H atau 1834-1919M) atau yang dikenal dengan nama Kiai Pacitan didirikan 
        oleh Ny. Ali/ ummu Ali yaitu putri dari Kiai Abdul Adzim ( dalam masa kisaran 1700M/1111H)
        Bersama suaminya (belum diketahui nama suami beliau), selanjutnya kepengasuhan 
        digantikan oleh anak beliau yang tertua yaitu kiai Ali pulasari, beliau memiliki beberapa 
        saudara yaitu kiai kayu ageng, kiai minhu,kiai dural, bindoro sahal, bindoro mahmud, bindoro 
        Wahab, Ny. qosan dan yang terakhir adalah Ny. Maryam. Setelah itu kepengasuhan digantikan 
        kepada ny. Qosan dengan suaminya dan kiai ali hijrah ke desa pulasari dan melanjutkan 
        dakwahnya disana. Namun tidak berselang lama Ny. Qosan juga hijrah ikut keruamh suaminya, 
        kemudian kepengasuhan dilanjutkan oleh Ny. Maryam dan suaminya.
        </p>

    <p>
        Ny. Maryam memiliki 2 putra-putri yang Bernama Kiai daningsih dan Ny. Ghoyyar (istri dari Kiai 
Ghoyyar yang selanjutnya dikenal dengan kiai Khoyyar). Dari pasangan kiai khoyyar dan 
istrinya melahir beberapa putra-putri diantara putranya adalah KH. Muhammad Sholeh, dan 
melalui pesan dari simbah kiai ali pulasari dan kiai kayu ageng beserta pamannya kiai 
daningsih bahwa kepengasuhan pondok diberikan kepada beliau KH. Sholeh/kiai pacitan yang 
waktu itu beliau terkenal Alim ‘allamah. Beliau mengasuh pondok pesantren al akhyar (yang 
masa itu masih dikenal dengan sebutan pondok beagung diambilkan dari nama dusun tambak 
agung) terbilang cukup lama karena beliau wafat saat umur 87 tepatnya tahun 1337H/1919M. 
Kiai pacitan memiliki 3 putra yaitu KH. Ismail ( yang melajutkan kepengasuhan beliau), KH. 
Dimyathi (beliau adalah pendiri pondok pesantren nurul iman los kolak), dan KH. Dahlan 
(pendiri pondok sanggre agung). Dan 2 orang putri.
    </p>

    <p>
        KH. Isma’il dilahirkan pada tahun 1287H/1870M dan wafat pada tahun 1387H/1967M, beliau 
melanjutkan kepengasuhan ayahnya diumur ke 50/49 tahun dihitung dari tahun wafatnya Kiai 
pacitan. Beliau KH Isma’il memiliki 2 orang putri Ny. Nafisah namun wafat dengan 
meninggalkan 1 putri Bernama Ny. Ma’rufah, dan yang ke 2 bernama Ny. Khadijah yang 
dinikahkan dengan santri beliau yang Bernama KH. Mahfudz Basuni. 
    </p>

    <p>
        Dua tahun sebelum wafatnya KH. Isma’il tepatnya pada tahun 1384H/1964M, KH. Umar Abd. 
Hamid suami dari Ny. Maryam bin Mahfudz (pendiri Pondok Pesantren Al Bar Sukolilo Timur)
mendirikan pendidikan klasikal untuk santri putra yang diberi nama Madrasah Islamiyah Al 
Akhyar (yang selanjutnya menjadi cikalbakal nama Pondok Pesantren), diambilkan dari nama 
Kiai Khoyyar yang merupakan kakek dari KH. Ismail Sholeh.
    </p>

    <p>
        Sepeninggalnya KH. Isma’il, kepengasuhan digantikan oleh KH. Mahfudz Basuni menantu 
beliau melalui penunjukan langsung. Beliau mengasuh selama 22 tahun, beliau memiliki 
banyak putra putri diantaranya adalah KH. Kholil mahfudz (PONPES KH. Abdul Hamid, sukolilo), 
KH. Khozen Mahfudz (Tragah), Ny. ‘Aidah Mahfudz ( istri KH> Toha yasin, pendiri pondok 
Assalafi labang), Ny. Sa’diyah Mahfudz ( Istri KH. Sholeh Zaini, Pendiri Pondok Pesantren Al 
Mubtadi’in Tragah).
    </p>

    <p>
        Pada tahun 1409H/1989M KH. Mahfudz wafat dan estafet kepengasuhan dilanjutkan oleh 
putra beliau KH. Hasan Mahfudz, dengan dibantu oleh saudara beliau KH. Fathurrahman 
mahfudz, KH. Musleh Mahfudz dan KH. Dzofir Sifan (suami dari Ny.Hj habibah Mahfudz). Pada 
tahun 1403H/ 1982M KH. Musleh Mahfudz mendirikan Madrasah Al Akhyar Putri (yang kemudian diganti nama Madrasah Shufiyatul Akhyar, 2005) tingkat Ibtida’iyah dan dilanjutkan
tingkat tsanawiyah pada tahun 1408H/1997M.
    </p>

    <p>
        Pada masa kepengasuhan KH. Hasan Mahfudz pengembangan pondok pesantren dilaksanakan 
dengan membagi santri ke beberapa komplek yaitu pondok timur (Darul Fiqhiyah, 2010), 
Pondok Barat (Darut Tauhid) dan Pondok Utara ( Darus Shufiyah), dan pada tahun 2005 
pengembangan pondok dilaksanakan dengan mendirikan sekolah formal swasta, jenjang MTS 
untuk santri putri dan SMP untuk santri putra berikut SMK. Pada tahun 1998H KH. 
Fathurrahman wafat, dilanjutkan oleh KH. Musleh Mahfudz pada tahun 2008, dan KH. Hasan 
Mahfudz pada Tahun 2009M. selanjutnya kepengasuhan pondok pesantren Al Akhyar
dilanjutkan oleh KH. Firdaus Hasan Bersama paman beliau KH. Dzofir Sifan (yang wafat pada 
tahun 2016). 
    </p>
